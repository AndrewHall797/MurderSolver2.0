
public class Event {

	private int time;
	private String device;
	private String ID;
	private String name;
	private String event;
	
	public Event(int time, String device, String ID, String name, String event) {
		this.time = time;
		this.device = device;
		this.ID = ID;
		this.name = name;
		this.event = event;
	}
}
